     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $all = Cart::where('user_ip',request()->ip())->get();
        if(count($all) > 0){
            $cart = Cart::where('user_ip',request()->ip())->get();
            if(session()->has('LoggedCustomer')){
                $customer = Customer::where('id','=',session('LoggedCustomer'))->first();
                $data = ['LoggedCustomerInfo'=>$customer];
            }
            $category = Category::where('status','active')->latest()->get();
            return view('frontend.checkout.checkout',compact('category','cart'),$data);
        }else{
            return redirect('/');
        }
    }

